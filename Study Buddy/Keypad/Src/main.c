void setRowPinsHigh(){
	HAL_GPIO_WritePin(ROW_PORT, ROW1_PIN, GPIO_PIN_SET);
	HAL_GPIO_WritePin(ROW_PORT, ROW2_PIN, GPIO_PIN_SET);
	HAL_GPIO_WritePin(ROW_PORT, ROW3_PIN, GPIO_PIN_SET);
    HAL_GPIO_WritePin(ROW_PORT, ROW4_PIN, GPIO_PIN_SET);
}

char scanButtonMatrix(){
	const char buttonMap[4][3] = {
	  		{'1', '2', '3'},
	  		{'4', '5', '6'},
	  		{'7', '8', '9'},
	  		{'L', '0', 'R'},
	  };
	for (int row = 0; row < 4; ++row) {
	        // Set all rows HIGH
		setRowPinsHigh();

	        // Set the current row LOW
		if (row == 0) HAL_GPIO_WritePin(ROW_PORT, ROW1_PIN, GPIO_PIN_RESET);
		if (row == 1) HAL_GPIO_WritePin(ROW_PORT, ROW2_PIN, GPIO_PIN_RESET);
		if (row == 2) HAL_GPIO_WritePin(ROW_PORT, ROW3_PIN, GPIO_PIN_RESET);
		if (row == 3) HAL_GPIO_WritePin(ROW_PORT, ROW4_PIN, GPIO_PIN_RESET);

	        // Check each column for a button press
		if (HAL_GPIO_ReadPin(COL_PORT, COL1_PIN) == GPIO_PIN_RESET) return buttonMap[row][0];
		if (HAL_GPIO_ReadPin(COL_PORT, COL2_PIN) == GPIO_PIN_RESET) return buttonMap[row][1];
		if (HAL_GPIO_ReadPin(COL_PORT, COL3_PIN) == GPIO_PIN_RESET) return buttonMap[row][2];
	}

	return 0;
}

void sendButtonPress(char button){
	HAL_UART_Transmit(&huart1, (uint8_t*)&button, 1, HAL_MAX_DELAY);
}

